public class BitManipulation {
    public static void main (String args[]) {
        System.out.println(200);
        System.out.println(200 & (1 << 3));

        System.out.println(1 << 10);
        System.out.println(1 << 1);
        System.out.println(10 & (1 << 1));
    }
}
